
# List assignment
[a, b] = [1, 2]
print(f"List assignment: a = {a}, b = {b}")  # Output: a = 1, b = 2

# Tuple assignment (unpacking a list)
x, y = [3, 4]
print(f"Tuple assignment (from list): x = {x}, y = {y}")  # Output: x = 3, y = 4

# Tuple assignment (naturally from tuple)
p, q = (5, 6)
print(f"Tuple assignment (from tuple): p = {p}, q = {q}")  # Output: p = 5, q = 6
