
# List Assignment
list_example = [1, 2, 3]
list_example[0] = 10  # Modifying the list
print(list_example)  # Output: [10, 2, 3]

# Tuple Assignment
tuple_example = (1, 2, 3)
# tuple_example[0] = 10  # This would raise a TypeError: 'tuple' object does not support item assignment
print(tuple_example)  # Output: (1, 2, 3)
