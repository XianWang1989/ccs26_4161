
# List assignment
list_assign = [1, 2]
[a, b] = list_assign  # Unpacking from a list
print("List assignment:")
print(f"a = {a}, b = {b}")

# Tuple assignment
tuple_assign = [3, 4]
c, d = tuple_assign  # Unpacking from a list (acting as a tuple here)
print("Tuple assignment:")
print(f"c = {c}, d = {d}")
