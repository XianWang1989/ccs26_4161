
# List assignment
list_assignment = [1, 2]
a, b = list_assignment

print("List Assignment:")
print("a =", a)  # Output: 1
print("b =", b)  # Output: 2

# Tuple assignment
tuple_assignment = (1, 2)  # Explicit tuple
c, d = tuple_assignment

print("\nTuple Assignment:")
print("c =", c)  # Output: 1
print("d =", d)  # Output: 2

# Unpacking from a list
e, f = [3, 4]
print("\nUnpacking from a list:")
print("e =", e)  # Output: 3
print("f =", f)  # Output: 4
