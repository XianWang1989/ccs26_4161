
# List assignment
[a, b] = [1, 2]
print(f"List Assignment -> a: {a}, b: {b}")  # Output: a: 1, b: 2

# Tuple assignment
a, b = [3, 4]
print(f"Tuple Assignment -> a: {a}, b: {b}")  # Output: a: 3, b: 4

# Both assignments yield similar results, but they are conceptually different:
# One explicitly uses a list structure [a, b], while the other is just a variable unpacking.
