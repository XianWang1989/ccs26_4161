
# List assignment
[a, b] = [1, 2]
print("List Assignment:")
print("a:", a)
print("b:", b)

# Tuple assignment (unpacking a list)
a, b = [3, 4]
print("\nTuple Assignment:")
print("a:", a)
print("b:", b)

# Tuple assignment (unpacking a tuple)
c, d = (5, 6)
print("\nTuple Assignment with a tuple:")
print("c:", c)
print("d:", d)
