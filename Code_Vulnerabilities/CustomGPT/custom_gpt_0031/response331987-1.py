
# List Assignment
list_a, list_b = [1, 2]  # Here, both sides are treated as lists
print("List Assignment:")
print(f"list_a = {list_a}, list_b = {list_b}")

# Tuple Assignment
tuple_a, tuple_b = (3, 4)  # Here, left side is treated as a tuple
print("Tuple Assignment:")
print(f"tuple_a = {tuple_a}, tuple_b = {tuple_b}")

# Assigning from a list to multiple variables (tuple unpacking)
x, y = [5, 6]
print("Tuple Assignment from List:")
print(f"x = {x}, y = {y}")
