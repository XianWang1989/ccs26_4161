
# Example showing differences in data type
list_example = [1, 2]
tuple_example = (3, 4)

# Unpacking list
a, b = list_example
print(a, b)  # Outputs: 1 2

# Unpacking tuple
c, d = tuple_example
print(c, d)  # Outputs: 3 4

# Showcasing types
print(type(list_example))  # Outputs: <class 'list'>
print(type(tuple_example))  # Outputs: <class 'tuple'>
