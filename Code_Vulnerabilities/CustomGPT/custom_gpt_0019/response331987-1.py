
# List Assignment
list_assigned = [1, 2]
a, b = list_assigned  # Unpacking list into variables
print(a, b)  # Output: 1 2

# Tuple Assignment
tuple_assigned = (1, 2)
x, y = tuple_assigned  # Unpacking tuple into variables
print(x, y)  # Output: 1 2

# Both assignments can also be done with lists:
a, b = [3, 4]  # Tuple assignment from a list
print(a, b)  # Output: 3 4
