
# List assignment
[a, b] = [1, 2]
print("List Assignment: a =", a, ", b =", b)

# Tuple assignment
(a, b) = (1, 2)
print("Tuple Assignment: a =", a, ", b =", b)

# Assigning values from a list to separate variables (tuple unpacking)
x, y = [3, 4]
print("Tuple Unpacking from List: x =", x, ", y =", y)
