
# List Assignment
[a, b] = [1, 2]
print("List Assignment:")
print(f"a = {a}, b = {b}")

# Tuple Assignment
a, b = (1, 2)
print("Tuple Assignment:")
print(f"a = {a}, b = {b}")

# Additional Example
# Showing list inside tuple assignment
a, b = [3, 4]  # Here [3, 4] is a list
print("List used in Tuple Assignment:")
print(f"a = {a}, b = {b}")
