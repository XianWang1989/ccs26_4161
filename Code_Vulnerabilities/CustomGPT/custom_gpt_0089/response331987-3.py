
# List Assignment
list_assignment = [10, 20]
[a, b] = list_assignment
print(f"List Assignment: a = {a}, b = {b}")

# Tuple Assignment
tuple_assignment = [30, 40]
(a, b) = tuple_assignment
print(f"Tuple Assignment: a = {a}, b = {b}")

# Mutable vs Immutable
my_list = [1, 2, 3]
my_list[0] = 10  # This is allowed

